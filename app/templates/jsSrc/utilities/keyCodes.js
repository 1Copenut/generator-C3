/**
 * This is a utility class for keyboard navigation 
 *
 * @main main
 */

/**
 * @class KeyCodes
 * @constructor
 */
class KeyCodes {
  constructor() {
    /**
     * @property tab
     * @type Number
     */
    this.tab = 9;

    /**
     * @property enter
     * @type Number
     */
    this.enter = 13;

    /**
     * @property esc
     * @type Number
     */
    this.esc = 27;

    /**
     * @property space
     * @type Number
     */
    this.space = 32;

    /**
     * @property pageup
     * @type Number
     */
    this.pageup = 33;

    /**
     * @property pagedown
     * @type Number
     */
    this.pagedown = 34;

    /**
     * @property end
     * @type Number
     */
    this.end = 35;

    /**
     * @property home
     * @type Number
     */
    this.home = 36;

    /**
     * @property left
     * @type Number
     */
    this.left = 37;

    /**
     * @property up
     * @type Number
     */
    this.up = 38;

    /**
     * @property right
     * @type Number
     */
    this.right = 39;

    /**
     * @property down
     * @type Number
     */
    this.down = 40;
  }
}

export default KeyCodes;
